    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String accion = request.getParameter("accion");
         HttpSession sesion = request.getSession();
         
         if(accion.equalsIgnoreCase("agregar")){
            String u = request.getParameter("email");
            String p = request.getParameter("pass");
            c = cd.validar(u, p);
            if(c.getEmail() == null){
            c.setEmail(u);
            c.setPassword(p);
            cd.agregar(c);
            request.getRequestDispatcher("controlador?menu=login").forward(request, response);
         }else{
                 sesion.setAttribute("me", "este usuario ya existe, intente con otro");
                 request.getRequestDispatcher("controlador?menu=registro").forward(request, response);
            }
         }
         
    }

